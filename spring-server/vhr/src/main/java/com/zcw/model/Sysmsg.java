package com.sample;


public class Sysmsg {

  private long id;
  private long mid;
  private long type;
  private long hrid;
  private long state;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getMid() {
    return mid;
  }

  public void setMid(long mid) {
    this.mid = mid;
  }


  public long getType() {
    return type;
  }

  public void setType(long type) {
    this.type = type;
  }


  public long getHrid() {
    return hrid;
  }

  public void setHrid(long hrid) {
    this.hrid = hrid;
  }


  public long getState() {
    return state;
  }

  public void setState(long state) {
    this.state = state;
  }

}
